package tandr.model;

public class Theater {

	private int theaterCode; 
	private String theaterLoc; 
	private String theaterName;
	
	public int getTheaterCode() {
		return theaterCode;
	}
	public void setTheaterCode(int theaterCode) {
		this.theaterCode = theaterCode;
	}
	public String getTheaterLoc() {
		return theaterLoc;
	}
	public void setTheaterLoc(String theaterLoc) {
		this.theaterLoc = theaterLoc;
	}
	public String getTheaterName() {
		return theaterName;
	}
	public void setTheaterName(String theaterName) {
		this.theaterName = theaterName;
	}
}
